// Initialize Language
const initLanguage = () => {
    const savedLang = localStorage.getItem('language') || 'en';
    document.documentElement.lang = savedLang;
    document.getElementById('language-select').value = savedLang;
    updateContent(savedLang);
};

// Update Content Based on Language
const updateContent = (lang) => {
    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });
    // Update document title
    const titleKey = document.querySelector('title').getAttribute('data-lang-key');
    document.title = translations[lang][titleKey] || translations[lang].title;
};

// Handle Language Change
const handleLanguageChange = (e) => {
    const lang = e.target.value;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updateContent(lang);
    // Announce language change for accessibility
    showNotification(lang === 'fa' ? 'ÿ≤ÿ®ÿßŸÜ ÿ®Ÿá ŸÅÿßÿ±ÿ≥€å ÿ™ÿ∫€å€åÿ± ⁄©ÿ±ÿØ' : 'Language changed to English', 'success');
};

// Show Notification
const showNotification = (message, type) => {
    const notification = document.createElement('div');
    notification.className = `notification ${type}`;
    notification.textContent = message;
    notification.setAttribute('role', 'alert');
    document.body.appendChild(notification);
    setTimeout(() => {
        notification.classList.add('fade-out');
        setTimeout(() => notification.remove(), 300);
    }, 3000);
};

// Scroll Reveal Animation
const revealSections = () => {
    const sections = document.querySelectorAll('.section-hidden');
    const observerOptions = {
        threshold: 0.15
    };
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    sections.forEach(section => observer.observe(section));
};

